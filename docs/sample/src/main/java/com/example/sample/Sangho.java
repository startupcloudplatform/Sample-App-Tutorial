package com.example.sample;

class Sangho {
    private String bdong;
    private String address;
    private Double months;
    private String hdong;
    private String lng;
    private String alive;
    private String sangho;
    private String lat;

    public String getBdong() {
        return bdong;
    }

    public void setBdong(String bdong) {
        this.bdong = bdong;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public Double getMonths() {
        return months;
    }

    public void setMonths(Double months) {
        this.months = months;
    }

    public String getHdong() {
        return hdong;
    }

    public void setHdong(String hdong) {
        this.hdong = hdong;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getAlive() {
        return alive;
    }

    public void setAlive(String alive) {
        this.alive = alive;
    }

    public String getSangho() {
        return sangho;
    }

    public void setSangho(String sangho) {
        this.sangho = sangho;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }
}
